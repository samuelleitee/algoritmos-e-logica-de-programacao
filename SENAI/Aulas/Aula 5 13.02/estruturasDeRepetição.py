# Entrada
a = int(input('Type a number for a: '))
b = int(input('Type a number for b: '))

# Processamento
if a > b:
    # Saída
    print('a is greater than b')
elif b > a:
    # Saída
    print('b is greater than a')
else:
    # Saída
    print('b is equal to a')

